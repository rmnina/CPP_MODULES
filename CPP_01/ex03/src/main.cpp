#include "../include/HumanA.hpp"
#include "../include/HumanB.hpp"

int	main(void)
{
	{
		HumanB jim("Jim");
		jim.attack();
	}	
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
		return (0);
}